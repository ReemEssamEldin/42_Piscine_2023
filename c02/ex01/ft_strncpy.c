#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	c;

	c = 0;
	while (c < n && src[c] != '\0')
	{
		dest[c] = src[c];
		c++;
	}
	while (c < n)
	{
		dest[c] = '\0';
		c++;
	}
	return (dest);
}

int	main()
{
	char	src[] = "Hello world!";
	char	dest[30];
	unsigned int	limit;

	limit = 5;
	printf("%s", ft_strncpy(dest, src, limit));
	return (0);
}
