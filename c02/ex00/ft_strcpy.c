#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char    dest[13];
	char    src[] = "This is ex00!";
	ft_strcpy(dest, src);
	printf("Copied string: %s \n", dest);
	return (0);
}
